from src.functions import sort_filtered_operations


def test_sort_filtered_operations():
    date = [
        {
            "id": 649467725,
            "date": "2019-04-14T19:35:28.978265",
        },
        {
            "id": 987654321,
            "date": "2018-04-14T19:35:28.978265",
        },
        {
            "id": 123456789,
            "date": "2020-04-14T19:35:28.978265",
        },
        {
            "id": 555444333,
            "date": "2021-04-14T19:35:28.978265",
        },
        {
            "id": 345345345555,
            "date": "2017-04-14T19:35:28.978265",
        },
        {
            "id": 89234512,
            "date": "2022-04-14T19:35:28.978265",
        },
    ]
    expected = [
        {
            "id": 89234512,
            "date": "2022-04-14T19:35:28.978265",
        },
        {
            "id": 555444333,
            "date": "2021-04-14T19:35:28.978265",
        },
        {
            "id": 123456789,
            "date": "2020-04-14T19:35:28.978265",
        },
        {
            "id": 649467725,
            "date": "2019-04-14T19:35:28.978265",
        },
        {
            "id": 987654321,
            "date": "2018-04-14T19:35:28.978265",
        },
    ]
    assert sort_filtered_operations(date) == expected
